package Book2_page65.Chapter03.NumbersAndExpressions.ArithmeticOperators.VariablesAndDataTypes.Strings.InitializingVariables;

// A final variable, also called a constant, is a variable whose value you can’t change
// after it’s been initialized.

/**
 * The type Final variables constants.
 */
public class FinalVariables_Constants {
	/**
	 * The entry point of application.
	 *
	 * @param args the input arguments
	 */
	public static void main(String[] args) {
      // To declare a final variable, you add the final keyword.
      final int WEEKDAYS = 5;

    System.out.println(WEEKDAYS);
  }
}
